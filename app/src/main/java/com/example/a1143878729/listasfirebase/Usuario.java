package com.example.a1143878729.listasfirebase;

public class Usuario {

    public String uid;
    public String nombre;
    public String correo;
    public String contra;

    public Usuario(){

    }

    public Usuario(String uid, String nombre, String correo, String contra) {
        this.uid = uid;
        this.nombre = nombre;
        this.correo = correo;
        this.contra = contra;
    }
}
